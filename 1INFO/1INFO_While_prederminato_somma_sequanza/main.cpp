#include <iostream>
using namespace std;
int main()
{
    int N; // variabile per la numerosità
    int cont = 0; // variabile contatore inizializzata a 0

    cout << "Inserisci la numerosità : ";
    cin >> N;   // acquisisco la numerosità da input

    while (cont < N) { // struttura base del ciclo




        cont++;
    }
    return 0;
}
